namespace SixLabors.ImageSharp.Formats.Ani;

/// <summary>
/// Specifies the format of the frame data.
/// </summary>
public enum AniFrameFormat
{
    /// <summary>
    /// The frame data is in ICO format.
    /// </summary>
    Ico = 1,

    /// <summary>
    /// The frame data is in CUR format.
    /// </summary>
    Cur,

    /// <summary>
    /// The frame data is in BMP format.
    /// </summary>
    Bmp
}
